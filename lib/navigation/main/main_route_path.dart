class MainRoutePath {
  final bool isGetStarted;
  final bool isHome;

  MainRoutePath.splashScreen()
      : isGetStarted = false,
        isHome = false;
  MainRoutePath.getStartedScreen()
      : isGetStarted = false,
        isHome = false;
  MainRoutePath.home()
      : isGetStarted = false,
        isHome = false;
}
